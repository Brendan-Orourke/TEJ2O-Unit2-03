let length = 5  // in cm
let width = 3   // in cm

// Calculate area and perimeter
let area = length * width
let perimeter = 2 * (length + width)

// Display area
basic.showString("A:")
basic.showNumber(area)

// Display perimeter
basic.showString("P:")
basic.showNumber(perimeter)
